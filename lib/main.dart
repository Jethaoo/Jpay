/// 5. MANAGE GROUPS SCREEN
class ManageGroupsScreen extends StatelessWidget {
  const ManageGroupsScreen({super.key});

  @override
  Widget build(BuildContext context) {
    final user = FirebaseAuth.instance.currentUser;

    return Scaffold(
      appBar: AppBar(
        title: const Text("Manage Ledgers"),
      ),
      body: StreamBuilder<QuerySnapshot>(
        stream: FirebaseFirestore.instance
            .collection('groups')
            .where('createdBy', isEqualTo: user?.uid)
            .orderBy('createdAt', descending: true)
            .snapshots(),
        builder: (context, snapshot) {
          if (snapshot.connectionState == ConnectionState.waiting) {
            return const Center(child: CircularProgressIndicator());
          }

          if (snapshot.hasError) {
            return Center(child: Text("Error: ${snapshot.error}"));
          }

          if (!snapshot.hasData || snapshot.data!.docs.isEmpty) {
            return Center(
              child: Column(
                mainAxisAlignment: MainAxisAlignment.center,
                children: [
                  Icon(Icons.inbox, size: 64, color: Colors.indigo.shade100),
                  const SizedBox(height: 16),
                  const Text("No groups to manage"),
                ],
              ),
            );
          }

          final groups = snapshot.data!.docs;

          return ListView.builder(
            padding: const EdgeInsets.all(8),
            itemCount: groups.length,
            itemBuilder: (context, index) {
              final group = groups[index];
              final groupName = group['name'];
              final totalOwed = group['totalOwed'] ?? 0.0;
              final friends = List.from(group['friends'] ?? []);

              return Card(
                elevation: 2,
                margin: const EdgeInsets.symmetric(vertical: 6, horizontal: 4),
                child: ListTile(
                  contentPadding: const EdgeInsets.symmetric(horizontal: 16, vertical: 8),
                  leading: CircleAvatar(
                    backgroundColor: Colors.indigo,
                    foregroundColor: Colors.white,
                    child: Text(groupName[0].toUpperCase()),
                  ),
                  title: Text(groupName, style: const TextStyle(fontWeight: FontWeight.bold)),
                  subtitle: Text("${friends.length} friend${friends.length != 1 ? 's' : ''} • Owed: RM ${totalOwed.toStringAsFixed(2)}"),
                  trailing: IconButton(
                    icon: const Icon(Icons.delete, color: Colors.red),
                    tooltip: "Delete Ledger",
                    onPressed: () => _showDeleteConfirmation(context, group.id, groupName),
                  ),
                ),
              );
            },
          );
        },
      ),
    );
  }

  void _showDeleteConfirmation(BuildContext context, String groupId, String groupName) {
    showDialog(
      context: context,
      builder: (context) => AlertDialog(
        title: const Text("Delete Ledger"),
        content: Text("Are you sure you want to delete \"$groupName\"? This will also delete all expenses in this ledger. This action cannot be undone."),
        actions: [
          TextButton(
            onPressed: () => Navigator.of(context).pop(),
            child: const Text("Cancel"),
          ),
          ElevatedButton(
            onPressed: () => _deleteGroup(context, groupId),
            style: ElevatedButton.styleFrom(
              backgroundColor: Colors.red,
              foregroundColor: Colors.white,
            ),
            child: const Text("Delete"),
          ),
        ],
      ),
    );
  }

  Future<void> _deleteGroup(BuildContext context, String groupId) async {
    try {
      // First, delete all expenses in the group
      final expensesSnapshot = await FirebaseFirestore.instance
          .collection('groups')
          .doc(groupId)
          .collection('expenses')
          .get();

      final batch = FirebaseFirestore.instance.batch();
      
      // Delete all expenses
      for (var expense in expensesSnapshot.docs) {
        batch.delete(expense.reference);
      }
      
      // Delete the group itself
      batch.delete(FirebaseFirestore.instance.collection('groups').doc(groupId));
      
      await batch.commit();

      if (context.mounted) {
        Navigator.of(context).pop(); // Close confirmation dialog
        ScaffoldMessenger.of(context).showSnackBar(
          const SnackBar(content: Text("Ledger deleted successfully")),
        );
      }
    } catch (e) {
      if (context.mounted) {
        Navigator.of(context).pop(); // Close confirmation dialog
        ScaffoldMessenger.of(context).showSnackBar(
          SnackBar(content: Text("Error deleting ledger: $e")),
        );
      }
    }
  }
}